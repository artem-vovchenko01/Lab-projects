# Лічильник
counter = 0

# Цикл перевіряє усі 6-значні числа. Рівність сум перших трьох цифр і останніх трьох
# перевіримо посимвольно за допомогою конвертацій типів даних
for i in range(100000, 1000000):
    i = str(i)
    num1 = int(i[0])+int(i[1])+int(i[2])
    num2 = int(i[3])+int(i[4])+int(i[5])
    # 2 умови мають виконуватися одночасно
    if num1 == num2 and num1 == 13:
        counter+=1

print(counter)

